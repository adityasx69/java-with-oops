package com.first_java_program;

public class BasicsOfConditionalAndLoops {
    static void main() {
        int a = 10;

        if(a == 10){
            System.out.println("Hey");
        }

        System.out.println();

        int i = 1;
        while(i != 5){
            System.out.println(i);
            i++;
        }

        System.out.println();

        for(int count = 1; count != 5; count++){
            System.out.println(count);
        }

    }
}
